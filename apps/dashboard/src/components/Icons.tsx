export function StatusDot({ status, className = '' }: { status: string; className?: string }) {
  const colors = {
    completed: 'bg-neon-green shadow-neon-green/50',
    running: 'bg-neon-cyan shadow-neon-cyan/50 animate-pulse',
    failed: 'bg-tesla-red shadow-tesla-red/50',
    pending: 'bg-spacex-steel shadow-spacex-steel/50',
  }

  const color = colors[status as keyof typeof colors] || colors.pending

  return (
    <span
      className={`inline-block w-2 h-2 rounded-full shadow-lg ${color} ${className}`}
    />
  )
}
